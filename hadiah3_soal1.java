		jumlah = aa + bb;
		
	System.out.println("Hasil aa + bb : " + jumlah);
		
		kali = aa*bb*cc;
		
	System.out.println("Hasil aa x bb x cc : " + kali);
		
		bagi = (int) (bb / cc);
	
